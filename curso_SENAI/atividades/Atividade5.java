package atividades;
/*
 * Classe 'Atividade5' criada para printar operacoes logicas
 * com as variaveis booleanas
 */
public class Atividade5 {
	public static void main(String[] args) {
		
		// Definindo variaveis booleanas
		boolean temAtestado = true;
		boolean estudanteAprovado = false;
		
		// Printando operacoes logicas
		System.out.println(temAtestado && estudanteAprovado);
		System.out.println(temAtestado || estudanteAprovado);
		System.out.println(estudanteAprovado == false);
	      
	   }
}
