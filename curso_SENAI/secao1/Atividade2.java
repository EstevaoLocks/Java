package secao1;
/**
 * Classe 'Atividade2' criada para treinar a identificacao do melhor
 * tipo de variavel para utilizar para cada valor
 */
public class Atividade2 {
	public static void main(String[] args) {
		
		//Declarando as variaveis utilizadas
		byte idade;
		short distancia;
		short diasAno;
		long populacaoMundial;
   
		//Atribuindo valores numericos as variaveis
		idade = 25;
		distancia = 2008;
		diasAno = 24500;
		populacaoMundial = 8000000000L;
		
		//Printando valores atribuidos as respectivas variaveis
		System.out.println(idade);
		System.out.println(distancia);
		System.out.println(diasAno);
		System.out.println(populacaoMundial);
   }

}
