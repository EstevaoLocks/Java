package atividades;
/*
 * Classe 'Desafio2' criada para realizar o calculo de tres
 * valores armazenados em variaveis
 */
public class Desafio2 {
	public static void main(String[] args) {
	       
		// Declarando variaveis notas
		int nota1 = 7;
		int nota2 = 8;
		int nota3 = 9;
		
		// Calculando media
		int qntNotas = 3;
		double media = (nota1 + nota2 + nota3) / qntNotas;
		
		// Printando media
		System.out.println("A média é: " + media);
	   }
}
